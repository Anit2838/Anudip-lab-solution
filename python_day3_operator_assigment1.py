# Taking input from the user
num = int(input("Enter a number: "))

# Using a ternary operator to check if the number is even or odd
result = "Even" if num % 2 == 0 else "Odd"

# Printing the result
print(f"The number is {result}.")

'''OUTPUT
Enter a number: 5
The number is Odd.

Enter a number: 6
The number is Even.'''
