#Declare a square() function with one parameter.Then call the function and pass one number and display the square of that number
# Define the square() function that takes one parameter n
def square(n):
    # Calculate the square of the number
    result = n * n
    # Print the result of the square
    print(f"The square of {n} is: {result}")

# Call the square() function with a number as an argument
square(5)

'''OUTPUT
The square of 5 is: 25'''
